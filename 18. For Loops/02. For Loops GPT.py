# for loops = execute a block of code a fixed number of times.
#             You can iterate over a range, string, sequence, etc.

x = 1
while x <= 10:
    print(x)
    x += 1
# In this version, it starts with x = 1 and keeps printing x until x becomes greater than 10.
# The x += 1 line increases x by 1 each time, so the loop eventually stops when x reaches 11.
